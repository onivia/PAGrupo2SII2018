package ejemplo3;

public class Programa {
    public static void main(String[] args) {
        
        int i = 2;        
        while(i <= 4) {            
            System.out.println(i);            
            
            i++;
        }             
        
        for (i = 2; i < 5; i++) {
            System.out.println(i);                        
        }
    }    
}
